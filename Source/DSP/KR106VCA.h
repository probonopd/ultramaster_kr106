// Table interpolation helper
static inline float VCALookup(const float* table, float envNorm)
{
  float idx = envNorm * 255.f;
  int i0 = static_cast<int>(idx);
  i0 = std::min(i0, 254);
  float frac = idx - i0;
  return table[i0] + frac * (table[i0 + 1] - table[i0]);
}

// J106 VCA: hardware-measured transfer function (Boaris clone chips).
// TR17 (2SA1015-Y PNP) + R95 22K emitter degeneration + BA662 OTA.
static inline float VCAGainJ106(float envNorm)
{
  return VCALookup(kVCATableHW, envNorm);
}

// J6/J60 VCA: circuit-model transfer function.
// Same BA662 OTA but driven by IR3109 control current (different from J106's
// discrete TR17 converter). Uses the original Shockley equation model until
// we have J6 hardware VCA measurements.
static inline float VCAGainJ6(float envNorm)
{
  return VCALookup(kVCATable, envNorm);
}

static inline float VCAGainJ60(float envNorm)
{
  return VCAGainJ6(envNorm); // same circuit as J6
}

// Dispatch by model
static inline float VCAGain(float envNorm, Model model)
{
  switch (model)
  {
    case kJ106: return VCAGainJ106(envNorm);
    case kJ60:  return VCAGainJ60(envNorm);
    default:    return VCAGainJ6(envNorm);
  }
}

